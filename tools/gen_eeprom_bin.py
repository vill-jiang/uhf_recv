def RunStepsFromDataCheck(steps_seq):
  idx = 0
  addr = 0
  while True:
    op = steps_seq[idx]
    if op == OP_END:
      print("EndFlag")
      return
    op_addr = op & 0xC0
    if op_addr == OP_ADDR_OLD:
      idx += 1
    elif op_addr == OP_ADDR_INC:
      addr += 1
      idx += 1
    elif op_addr == OP_ADDR_ONE_BYTE:
      addr = steps_seq[idx + 1]
      idx += 2
    elif op_addr == OP_ADDR_TWO_BYTES:
      addr = steps_seq[idx + 1] << 8 | steps_seq[idx + 2]
      idx += 3
    if addr == 0xFFFF:
      print("EndFlag")
      return
    i = 0
    mask = 0x03
    reg = 0
    print("NewAddr: {:04X}".format(addr))
    while True:
      masked_op = op & mask
      if masked_op == OP_DELAY_READ:
        print('  Delay(1)\n  Read({:02X})'.format(addr))
      elif masked_op == OP_READ_ADDR:
        print('  Read({:04X})'.format(addr))
        reg = 0x00
      elif masked_op == OP_READ_ROM:
        print('  Rom({:02X})'.format(steps_seq[idx + i]))
        reg = steps_seq[idx + i]
        i += 1
      elif masked_op == OP_AND:
        print('   RegAnd({:02X})'.format(steps_seq[idx + i]))
        reg &= steps_seq[idx + i]
        i += 1
      elif masked_op == OP_OR:
        print('   RegOr({:02X})'.format(steps_seq[idx + i]))
        reg |= steps_seq[idx + i]
        i += 1
      elif masked_op == (OP_AND | OP_OR):
        print('   RegAnd({:02X})'.format(steps_seq[idx + i]))
        reg &= steps_seq[idx + i]
        i += 1
        print('   RegOr({:02X})'.format(steps_seq[idx + i]))
        reg |= steps_seq[idx + i]
        i += 1
      elif masked_op == OP_WRITE:
        print('    Write({:04X}, {:02X})'.format(addr, reg))
      elif masked_op == OP_LOOP:
        print('    if ({:02X} == {:02X}) i = 0; mask = 0x03'.format(reg, steps_seq[idx + i]))
        i += 1
      elif masked_op == OP_READ_RAM_WRITE:
        print('    Write({:04X}, pub_data[{:02X}])'.format(addr, reg))
      else:
        if masked_op == 0x00 and mask == 0x0C:
          pass
        else:
          raise Exception("ErrorOp")
      mask <<= 2
      if mask >= 0xC0:
        break
    idx += i

def convert_image_to_ssd1106_bmp(image_path, max_width=OLED_WIDTH, max_height=OLED_HEIGHT, threshold=240):
    """
    读取图片并转换为 ssd1106_draw_bmp 所需的位图数据格式。

    SSD1106 的位图格式：按 page 存储，每个 page 8 像素高。
    每个字节代表一列中 8 个垂直像素，LSB 在上，MSB 在下。
    数据按 page 从上到下、每个 page 内按列从左到右排列。

    返回: (bmp_data, bmp_width, bmp_height_pages, x0, y0, x1, y1)
      - bmp_data: 位图字节列表
      - bmp_width: 位图宽度（像素）
      - bmp_height_pages: 位图高度（page 数）
      - x0, y0, x1, y1: 居中绘制时的起始/结束坐标
    """
    img = Image.open(image_path)

    # 兼容带透明通道的 PNG 和普通无透明通道的 JPG：
    # 如果图片有 alpha 通道，先合成到白色背景上（透明区域变白），再转灰度
    if img.mode in ('RGBA', 'LA') or (img.mode == 'P' and 'transparency' in img.info):
        # 创建白色背景
        background = Image.new('RGBA', img.size, (255, 255, 255, 255))
        img = img.convert('RGBA')
        # 将原图合成到白色背景上，透明区域显示为白色
        background.paste(img, mask=img.split()[3])  # 使用 alpha 通道作为 mask
        img = background

    # 转换为灰度图
    img = img.convert('L')
    orig_w, orig_h = img.size
    print("原始图片尺寸: {}x{}".format(orig_w, orig_h))

    # 计算缩放比例，保持宽高比，高度对齐到 8 像素
    scale = min(max_width / orig_w, max_height / orig_h)
    new_w = int(orig_w * scale)
    new_h = int(orig_h * scale)

    # 高度向下对齐到 8 的倍数（page 对齐）
    new_h = (new_h // 8) * 8
    if new_h == 0:
        new_h = 8

    # 根据对齐后的高度重新计算宽度以保持宽高比
    new_w = int(orig_w * (new_h / orig_h))
    if new_w > max_width:
        new_w = max_width

    print("缩放后尺寸: {}x{} ({} pages)".format(new_w, new_h, new_h // 8))

    # 缩放图片（使用高质量的 LANCZOS 重采样）
    img = img.resize((new_w, new_h), Image.LANCZOS)

    # 二值化（反色模式）：像素值 >= threshold 为亮点，< threshold 为灭点
    # 原图黑色图标白色背景 → OLED 上白色背景亮、黑色图标灭（反色效果）
    pixels = list(img.getdata())

    # 转换为 SSD1106 page 格式
    pages = new_h // 8
    bmp_data = []
    bmp_light_cnt = 0

    for page in range(pages):
        for col in range(new_w):
            byte_val = 0
            for bit in range(8):
                row = page * 8 + bit
                pixel = pixels[row * new_w + col]
                # 像素值 < threshold 表示黑色，在 OLED 上点亮
                if pixel < threshold:
                    byte_val |= (1 << bit)
                    bmp_light_cnt += 1
            bmp_data.append(byte_val)

    # 计算居中坐标
    x0 = (OLED_WIDTH - new_w) // 2
    y0 = (OLED_PAGES - pages) // 2
    x1 = x0 + new_w
    y1 = y0 + pages

    print("位图数据大小: {} 字节".format(len(bmp_data)))
    print("位图点亮像素数: {} 个".format(bmp_light_cnt))
    print("居中坐标: x0={}, y0={}, x1={}, y1={}".format(x0, y0, x1, y1))
    print("C 调用: ssd1106_draw_bmp({}, {}, {}, {}, start_icon_bmp)".format(x0, y0, x1, y1))

    return bmp_data, new_w, pages, x0, y0, x1, y1

