        data_len = 0
        if menu_def[0] == MENU_ITEM_TYPE_BACK:
          pass
          # static_val_type_data.append(0)  # 无效数据占位
        elif menu_def[0] == MENU_ITEM_TYPE_INT8:
          static_val_type_data.append(menu_def[2][0])
          static_val_type_data.extend(menu_def[2][1].to_bytes(1, byteorder='little', signed=True))
          static_val_type_data.extend(menu_def[2][2].to_bytes(1, byteorder='little', signed=True))
          static_val_type_data.extend(menu_def[2][3].to_bytes(1, byteorder='little', signed=True))
          data_len = menu_def[2][0] + 1
          menu_data_def.append(('int8_t', inflection.underscore(menu_name)))
        elif menu_def[0] == MENU_ITEM_TYPE_INT32:
          static_val_type_data.append(menu_def[2][0])
          static_val_type_data.extend(menu_def[2][1].to_bytes(4, byteorder='little', signed=True))
          static_val_type_data.extend(menu_def[2][2].to_bytes(4, byteorder='little', signed=True))
