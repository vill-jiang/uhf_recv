# 每个2B的地址寄存器都有5种操作，读R，读E，写W，与&，或|，循环L，延时D
# 其中 读R 是读到临时变量中
#      与&，或| 是在临时变量中进行操作 并且
#      读E 是直接将数据读到临时变量中
#      写W 是将临时变量写入到地址中
#      循环L 判断临时变量和给定值是否相等，相等则继续循环
#      延时D 延时1ms
# 必然只有三步
#   1. 读E  读R  延时D 后读R
#   2. 与&  或|
#   3. 写W  循环L
# 每条指令可以用多个二进制字节表示
#   1. 最开头2B为地址，定义一个特殊值0xFFFF表示结束全部结束
#   2. 随后1B表示操作类型，每2bit按位定义，相当于步骤细分为三步 读->位操作->写或退出
#       0b____01 延时D 后读R
#       0b____10 读R
#       0b____11 读E 需要追加1B的数据
#       0b__01__ 与A 需要追加1B的数据
#       0b__10__ 或O 需要追加1B的数据
#       0b__11__ 与A 再 或O 需要追加2B的数据
#       0b01____ 写W
#       0b10____ 循环L 需要追加1B的数据
#       0b11____ 写公共缓存数据
#   3. C代码见 kt0656m_v11_drv.c
#   4. Python检查代码如下 RunStepsFromDataCheck

# 定义操作类型
OP_END            = 0b00000000  # 0x00
OP_ADDR_OLD       = 0b00000000  # 0x00
OP_ADDR_INC       = 0b01000000  # 0x40
OP_ADDR_ONE_BYTE  = 0b10000000  # 0x80
OP_ADDR_TWO_BYTES = 0b11000000  # 0xC0

OP_DELAY_READ     = 0b00000001  # 0x01
OP_READ_ADDR      = 0b00000010  # 0x02
OP_READ_ROM       = 0b00000011  # 0x03
OP_AND            = 0b00000100  # 0x04
OP_OR             = 0b00001000  # 0x08
OP_WRITE          = 0b00010000  # 0x10
OP_LOOP           = 0b00100000  # 0x20
OP_READ_RAM_WRITE = 0b00110000  # 0x30

